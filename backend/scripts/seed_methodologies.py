async def seed_one(session: Any, spec: dict[str, Any]) -> tuple[bool, int]:
    existing = (
        await session.execute(
            select(Methodology).where(Methodology.name == spec["name"])
        )
    ).scalar_one_or_none()
    if existing is not None:
        return False, existing.id

    methodology = Methodology(
        name=spec["name"],
        description=spec.get("description"),
        category=spec["category"],
        status="published",
        published_at=datetime.now(UTC),
    )
    session.add(methodology)
    await session.flush()

    scales: list[Scale] = []
    for i, sc in enumerate(spec["scales"]):
        scale = Scale(
            methodology_id=methodology.id,
            name=sc["name"],
            description=sc.get("description"),
            interpretation_low=sc.get("interpretation_low"),
            interpretation_mid=sc.get("interpretation_mid"),
            interpretation_high=sc.get("interpretation_high"),
            order_index=i,
        )
        session.add(scale)
        scales.append(scale)
    await session.flush()

    for i, q in enumerate(spec["questions"]):
        question = Question(
            methodology_id=methodology.id,
            text=q["text"],
            order_index=i,
            theme_tags=q.get("theme_tags"),
        )
        session.add(question)
        await session.flush()
        for scale_idx, weight in q.get("scales", []):
            session.add(
                QuestionScale(
                    question_id=question.id,
                    scale_id=scales[scale_idx].id,
                    weight=Decimal(str(weight)),
                )
            )

    await session.commit()
    return True, methodology.id


async def main() -> None:
    created = 0
    skipped = 0
    async with AsyncSessionLocal() as session:
        for spec in METHODOLOGIES:
            was_created, methodology_id = await seed_one(session, spec)
            label = "created" if was_created else "skipped"
            print(
                f"[seed] {label}: {spec['name']} id={methodology_id} "
                f"({len(spec['scales'])} scales, {len(spec['questions'])} questions)"
            )
            if was_created:
                created += 1
            else:
                skipped += 1
    print(f"[seed] done: created={created} skipped={skipped} total={len(METHODOLOGIES)}")


if __name__ == "__main__":
    asyncio.run(main())
